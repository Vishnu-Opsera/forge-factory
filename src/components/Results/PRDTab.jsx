import { useEffect, useState } from 'react';
import { motion } from 'framer-motion';
import { FileText, BookOpen } from 'lucide-react';

function PRDContent({ prd }) {
  const [html, setHtml] = useState('');

  useEffect(() => {
    if (!prd) return;
    import('marked').then(({ marked }) => {
      marked.setOptions({ breaks: true, gfm: true });
      setHtml(marked.parse(typeof prd === 'string' ? prd : JSON.stringify(prd, null, 2)));
    });
  }, [prd]);

  if (!prd) return null;

  return html ? (
    <div className="prd-content max-w-none" dangerouslySetInnerHTML={{ __html: html }} />
  ) : (
    <pre className="stream-text whitespace-pre-wrap text-slate-400">{prd}</pre>
  );
}

export default function PRDTab({ prd }) {
  if (!prd) {
    return <div className="glass-card p-8 text-center text-slate-600">No PRD generated.</div>;
  }

  return (
    <motion.div initial={{ opacity: 0 }} animate={{ opacity: 1 }} className="glass-card">
      {/* Header */}
      <div className="flex items-center gap-3 px-6 py-4 border-b border-slate-800/60">
        <div className="w-9 h-9 rounded-xl bg-forge-amber/10 border border-forge-amber/20 flex items-center justify-center">
          <FileText className="w-4 h-4 text-forge-amber" />
        </div>
        <div>
          <div className="font-semibold text-white text-sm">Product Requirements Document</div>
          <div className="text-xs text-slate-500">Generated by Forge AI · Ready for stakeholder review</div>
        </div>
        <div className="ml-auto">
          <div className="flex items-center gap-1.5 text-xs text-forge-amber bg-forge-amber/10 border border-forge-amber/20 px-2.5 py-1 rounded-full">
            <BookOpen className="w-3 h-3" />
            AI Generated
          </div>
        </div>
      </div>

      {/* Content */}
      <div className="px-8 py-6">
        <PRDContent prd={prd} />
      </div>
    </motion.div>
  );
}
